#include <iostream>

using namespace std;

void Literals()
{
	// constant integer literal
	const int intVal = 10;
	cout << "Integer Literal: " << intVal << endl;

	// constant float literal
	const float floatVal = 4.14;
	cout << "Floating point literal: " << floatVal << endl;

	// constant char literal
	const char charVal = 'A';
	cout << "Character Literal: " << charVal << endl;

	// constant string literal (array of char)
	const char str[] = "Welcome To Ramjilal4You";
	cout << "String Literal: " << str << endl;

}

